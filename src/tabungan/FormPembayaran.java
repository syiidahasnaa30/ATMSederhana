    private void bOVOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOVOActionPerformed
        boolean cek;
        Pembayaran bayar = new Pembayaran();
        String noTelpon =JOptionPane.showInputDialog("Masukan noTelpon: ");
        double jumlah = Double.parseDouble(JOptionPane.showInputDialog("Masukkan jumlah Top UP"));
        double saldo=0;
        bayar.setJumlah(jumlah);
        cek=bayar.bayar(noRekening, noTelpon, jumlah);
        if(cek==true){
            for(int i=0; i<Akun.totalUser();i++){
                if(Akun.akun.get(i).getNoRekening()==noRekening){
                     saldo=Akun.akun.get(i).getTabungan().getSaldo();
                     break;
                }
            }
           JOptionPane.showMessageDialog(this," TOP UP OVO anda berhasil\nSisa saldo anda"+(int)saldo);
        }else{
            JOptionPane.showMessageDialog(this," TOP UP OVO gagal, saldo tidak cukup ");
        }
    }//GEN-LAST:event_bOVOActionPerformed

