    private void bListrikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bListrikActionPerformed
        String noPelanggan =JOptionPane.showInputDialog("Masukan noPelanggan: ");
        boolean cek;
        //polimorfisme
        Pembayaran bayar = new Listrik();
        bayar.setNoPelanggan(noPelanggan);
        double jumlah=bayar.getJumlah();
        JOptionPane.showMessageDialog(this,"Tagihan listrik anda = "+jumlah);
        double saldo=0;
        cek=bayar.bayar(noRekening,noPelanggan, jumlah);
        if(cek==true){
            for(int i=0; i<Akun.totalUser();i++){
                if(Akun.akun.get(i).getNoRekening()==noRekening){
                     saldo=Akun.akun.get(i).getTabungan().getSaldo();
                     break;
                }
            }
            JOptionPane.showMessageDialog(this,"Bayar Tagihan Listrik Berhasil\n Sisa saldo anda"+(int)saldo);
        }else{
            JOptionPane.showMessageDialog(this," Bayar Tagihan Listrik anda gagal, saldo tidak cukup ");
        }
    }//GEN-LAST:event_bListrikActionPerformed

